def encrypt(message):
    return ' '.join([MORSE_CODE[char.upper()] if char != ' ' else '/' for char in message])


def decrypt(message):
    message_list = message.split(' ')
    inv_map = {v: k for k, v in MORSE_CODE.items()}
    
    word = ''
    for char in message_list:
        if char == '/':
            word += ' '
        else:
            word += inv_map[char]
    
    return word

